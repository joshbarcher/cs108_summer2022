# careful when working with numbers (integers, floats)
oper1 = 12.2
oper2 = 23.7

# print out the types of our operands
print("oper1 type", type(oper1))
print("oper2 type", type(oper2))

# add together and see the type of the result
resultAdd = oper1 + oper2
print("resultAdd is", resultAdd)
print("resultAdd type", type(resultAdd))

